    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String BookID = BookIDF.getText();
        String Title = TitleF.getText();
        String Publish = PublishF.getText();
        String PYear = PYearF.getText();
        String Stock = StockF.getText();

        try {

        if (isBookExists(BookID)) {
            manageData.updateBook(BookID, Title, Publish, PYear, Stock);
            JOptionPane.showMessageDialog(null, "Data successfully updated.");
        } else {
            manageData.saveBook(BookID, Title, Publish, PYear, Stock);
            JOptionPane.showMessageDialog(null, "Data successfully saved.");
        }
        loadDataToTable(); 
        ClearFields(); 
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(null, "Error saving data: " + e.getMessage());
    }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if (selectedRow >= 0) {

            BookIDF.setText((String) jTable1.getValueAt(selectedRow, 0));
            TitleF.setText((String) jTable1.getValueAt(selectedRow, 1));
            PublishF.setText((String) jTable1.getValueAt(selectedRow, 2));
            PYearF.setText((String) jTable1.getValueAt(selectedRow, 3));
            StockF.setText((String) jTable1.getValueAt(selectedRow, 4));
        } else {
            JOptionPane.showMessageDialog(null, "Please select a member to edit.");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        int selectedRow = jTable1.getSelectedRow();
        if (selectedRow >= 0) {
            String BookID = (String) jTable1.getValueAt(selectedRow, 0);
            int confirm = JOptionPane.showConfirmDialog(null, "Are you sure you want to delete this Book?", "Confirm Delete", JOptionPane.YES_NO_OPTION);
            if (confirm == JOptionPane.YES_OPTION) {
                try {
                    manageData.deleteBook(BookID);
                    loadDataToTable();
                    JOptionPane.showMessageDialog(null, "Book deleted successfully.");
                } catch (SQLException e) {
                    JOptionPane.showMessageDialog(null, "Error deleting Book: " + e.getMessage());
                }
            }
        } else {
            JOptionPane.showMessageDialog(null, "Please select a Book to delete.");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

