    private boolean isBookExists(String BookID) throws SQLException {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        for (int i = 0; i < model.getRowCount(); i++) {
            if (model.getValueAt(i, 0).equals(BookID)) {
                return true;
            }
        }
        return false;
    }
